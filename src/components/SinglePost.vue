<template>
  <div class="post">
    <h3>{{ post.title }}</h3>
    <p>{{ snippet }}</p>
    <span v-for="tag in post.tags" :key="tag">
    #{{ tag }}</span>
  </div>
<!--
-->

</template>

<script>
import { computed } from '@vue/reactivity'
// Write a script that displays only part of the post.body

export default {
    name: 'SinglePost',
    props: ['post'],
    setup(props) {
        const snippet = computed(() => {
            return props.post.body.substring(0,100) + '...'
        })
        
        return { snippet }
    }

}
</script>

